  # Navigation helpers for sidebar
  def nav_active?(path_pattern)
    case path_pattern
    when "/overview"
      # Match /dashboard/projects/{uuid} exactly (not any sub-routes)
      request.path.match?(%r{^/dashboard/projects/[^/]+$})
    when "/project_settings"
      # Match /dashboard/projects/{uuid}/edit exactly (not /secrets/{id}/edit etc.)
      request.path.match?(%r{/dashboard/projects/[^/]+/edit$})
    else
      request.path.include?(path_pattern)
    end
  end

  def nav_link_class(path_pattern)
    "dm-nav-link #{nav_active?(path_pattern) ? 'dm-nav-link-active' : ''}"
  end

  def action_badge_class(action)
    case action
    when /create/
      "badge-green"
    when /update/, /set/
      "badge-blue"
    when /delete/, /archive/, /revoke/
      "badge-red"
    when /read/, /get/, /list/, /export/
      "badge-gray"
    when /rollback/
      "badge-yellow"
    when /import/
      "badge-orange"
    else
      "badge-gray"
    end
  end

